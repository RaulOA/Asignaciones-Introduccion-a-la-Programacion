num1=int(input("Digite el 1er numero:   "))
num2=int(input("Digite el 2do numero:   "))

print("La suma de sus numeros da como resultado:    ",num1+num2)
print("La resta de sus numeros da como resultado:    ",num1-num2)
print("La multiplicacion de sus numeros da como resultado:    ",num1*num2)
print("La division de sus numeros da como resultado:    ",num1/num2)